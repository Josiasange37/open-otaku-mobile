const _videoUrl =
    'https://commondatastorage.googleapis.com/gtv-videos-bucket/sample/BigBuckBunny.mp4';

String _thumbUrl(int seed) =>
    'https://picsum.photos/seed/otaku_t$seed/320/180';

List<Episode> _generateEpisodes(String animeId, int count) {
  return List.generate(count, (i) {
    return Episode(
      id: '${animeId}_ep${i + 1}',
      number: i + 1,
      title: 'Episode ${i + 1}',
      thumbnailUrl: _thumbUrl(animeId.hashCode + i),
      duration: Duration(minutes: 22 + (i % 5)),
      watchedProgress:
          i < 2 ? Duration(minutes: 10 + i * 3) : Duration.zero,
      videoUrl: _videoUrl,
      isDownloaded: i < 1,
    );
  });
}

